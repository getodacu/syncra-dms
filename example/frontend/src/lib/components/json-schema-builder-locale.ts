type JsonJoyTranslation = Readonly<Record<keyof typeof jsonJoyRomanianLocale, string>>;

export const englishJsonJoyBuilderMessages = {
	schemaTypeObject: "Group"
} as const satisfies Partial<JsonJoyTranslation>;

export type JsonJoyBuilderConfig = {
	locale?: JsonJoyTranslation;
	messages?: Partial<JsonJoyTranslation>;
};

export function jsonJoyBuilderConfigForLocale(locale: string): JsonJoyBuilderConfig {
	if (locale === "ro") {
		return { locale: jsonJoyRomanianLocale };
	}

	return { messages: englishJsonJoyBuilderMessages };
}
